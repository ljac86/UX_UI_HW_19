$(document).ready(function() {
    $('.travelEAZE').hover(
        function() {
            // Mouse enters
            $(this).addClass('enlarged');
        },
        function() {
            // Mouse leaves
            $(this).removeClass('enlarged');
        }
        
    );  
});

